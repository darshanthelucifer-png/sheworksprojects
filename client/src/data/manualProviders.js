// Helper functions
export const getProvidersByServiceId = (serviceId) => {
  return manualProviders.filter(provider => provider.serviceId === serviceId && provider.isActive);
};

export const getProvidersByCategory = (category) => {
  return manualProviders.filter(provider => provider.category === category && provider.isActive);
};

export const getProvidersBySubCategory = (subCategory) => {
  return manualProviders.filter(provider => provider.subCategory === subCategory && provider.isActive);
};

export const getFeaturedProviders = () => {
  return manualProviders.filter(provider => provider.featured && provider.isActive);
};

export const getProviderById = (id) => {
  return manualProviders.find(provider => provider.id === id);
};

export const getAllCategories = () => {
  return [...new Set(manualProviders.map(provider => provider.category))];
};

export const getSubCategoriesByCategory = (category) => {
  return [...new Set(manualProviders
    .filter(provider => provider.category === category)
    .map(provider => provider.subCategory)
  )];
};

// Search providers by name, service, or description
export const searchProviders = (query) => {
  const lowerQuery = query.toLowerCase();
  return manualProviders.filter(provider => 
    provider.isActive && (
      provider.name.toLowerCase().includes(lowerQuery) ||
      provider.service.toLowerCase().includes(lowerQuery) ||
      provider.description.toLowerCase().includes(lowerQuery) ||
      provider.category.toLowerCase().includes(lowerQuery) ||
      provider.subCategory.toLowerCase().includes(lowerQuery)
    )
  );
};

// Get providers by location
export const getProvidersByLocation = (location) => {
  const lowerLocation = location.toLowerCase();
  return manualProviders.filter(provider => 
    provider.isActive && provider.location.toLowerCase().includes(lowerLocation)
  );
};

// Get top rated providers
export const getTopRatedProviders = (limit = 10) => {
  return manualProviders
    .filter(provider => provider.isActive)
    .sort((a, b) => b.rating - a.rating)
    .slice(0, limit);
};

export default manualProviders;